    <h4>Carrièreankers</h4>

</div>




<div class="box2">

    <p5><b>Je uitslag</b></p5>

</div>

<div class="container11">

    <p3>Op basis van je antwoorden is bepaald in hoeverre elke van de negen carrièreankers aansluit bij je persoonlijkheid. De mate waarin is weergegeven met een aantal punten.<br>
        De negen drijfveren zijn "rijkdom", "normen en waarden", "creativiteit", "onafhankelijkheid", "zekerheid", "macht", "ergens goed in zijn", "sociale contacten" en "status"<br>
        In totaal heb je 100 punten verdeeld. Een carrièreanker kan maximaal 25 punten hebben. Hieronder staan eerst de twee carrièreankers die je de meeste punten gaf en daaronder de carrièreanker die je het minst aantal punten gaf.<br>
        De meeste punten gaf je aan de rol: Creativiteit (18 punten)<br>
        <br>
        Voor iemand die creativiteit als carrièreanker heeft, is produceren van nieuwe ideeën of producten het voornaamste doel. De drijfveer is vernieuwend bezig zijn. Een werkomgeving die procedureel is en weinig ruimte laat voor eigen initiatief kan als zeer beperkend ervaren worden. Vaak werken creatieve mensen het liefst zelfstandig of in kleine teams.<br>
        Daarna gaf je de meeste punten aan: Onafhankelijkheid (18 punten)<br>
        <br>
        Autonomie is een belangrijke drijfveer. Het gaat mensen met dit carrièreanker om onafhankelijkheid en zelf beslissingen kunnen nemen. Het is dan ook niet verwonderlijk dat deze mensen vaak eigen baas zijn of dat graag willen worden. In hun loopbaankeuze laten zij zich dan ook leiden door de vraag in welke mate zij hun eigen gang kunnen gaan of zelfstandig kunnen werken.<br>
        Het minst aantal punten gaf je voor: Sociale contacten (0 punten)<br>
        <br>
        Mensen met deze drijfveer hechten veel waarde aan sociale contacten. Gezelligheid en vriendschap zijn belangrijke redenen voor het hebben van een baan. De scheiding tussen werk en privé geldt voor hun minder strikt. Bij loopbaankeuzes zal men zich dan ook snel laten leiden door de vraag of men wel voldoende collega's ontmoet of heeft. Of het werk bevalt hangt af van de mate waarin men kan samenwerken of onder de mensen is.<br>
        <br>
        Tot slot<br>
        Deze korte carrièreankerstest geeft een indicatie van je werkwaarden en daarbij behorende ideale werkomgeving. Omdat deze test vrij kort is en niet genormeerd is, kun je aan de uitslag geen verregaande conclusies verbinden. Op 123test.nl kun je ook een genormeerde en uitgebreide werkwaardentest doen. Deze test geeft aan welke werkwaarden en bedrijfscultuur jou het gelukkigst zullen maken. Voor deze uitgebreide werkwaardentest wordt wel een vergoeding gevraagd. </p3>

