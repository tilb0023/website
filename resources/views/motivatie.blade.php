<div class="box2">

    <p5><b>Wat is motivatie?</b></p5>

</div>

<div class="container11">

    <p3>Volgens Robert Tanner (What is Motivation?, 2019)
        is motivatie het sterke gevoel om iets te willen doen.
        Motivatie komt van behoeftes, waarden en doelen. Op Leren.nl (Wat is Motivatie, z.d.)
        definiëren ze motivatie als: “Motivatie is het gevoel dat je aanzet tot het beginnen én afmaken van een taak.
        Of nog ruimer, dat je aanzet tot bepaald gedrag. Met dat gedrag denk je iets te krijgen dat je nodig hebt.”<br>
        Samengevat is motivatie het gevoel om een bepaalde actie te maken. </p3>

</div>





<div class="box2">

    <p5><b>Intrinsieke en extrinsieke motivatie</b></p5>

</div>

<div class="container11">

    <p3>Volgens Kenda Cherry (Extrinsic vs. Intrinsic Motivation: What’s the Difference?, 2019) zijn er zijn twee soorten motivatie: Intrinsieke en extrinsieke motivatie.<br>
        Maar wat is intrinsieke en extrinsieke motivatie?<br>
        Bij extrinsieke motivatie worden we beïnvloed van buitenaf: We doen iets omdat we een beloning krijgen of we doen iets om een straf te voorkomen.
        Bij extrinsieke motivatie doen we dus een bepaalde taak niet omdat we het zelf willen doen.<br>
        Voorbeeld: studeren voor een goed cijfer.

        Als het maken van een taak of opdracht leuk is om te doen dan ben je intrinsiek gemotiveerd.
        Er is geen beloning voor het afmaken van de taak, in plaats daarvan wordt de taak gemaakt omdat het persoonlijk verrijkend is.
        Voorbeeld: studeren omdat het vak interessant is. </p3>

</div>





<div class="box2">

    <p5><b>Maar wat is beter: intrinsieke of extrinsieke motivatie?</b></p5>

</div>

<div class="container11">

    <p3>In de Ted Talk van Dan Pink (The Puzzle of Motivation) zegt hij dat we in het bedrijfsleven voornamelijk extrinsiek gemotiveerd worden. Hij beargumenteerd dat dit schadelijk is voor bedrijven en de werknemers. Het is namelijk wetenschappelijk bewezen dat mensen minder gemotiveerd raken als ze een beloning beloofd krijgen.<br>
        Dit klinkt in eerste instantie best vreemd, beloningen horen aan te sporen om beter te werken, maar beloningen zorgen ervoor dat we problemen proberen op te lossen met een bekrompen visie. Kortom, we gaan minder creatief denken.<br>
        De problemen die wij hedendaags moeten oplossen in het bedrijfsleven vergen creativiteit daarom werken beloningen dus averechts.<br>
        De opvattingen van Dan Pink zie je terug in de volgende twee artikelen:</p3>

</div>




<div class="box2">

    <p5><b>Linda van der Veen (De motivatie van software testend Nederland, z.d.):</b></p5>

</div>

<div class="container11">

    <p3>“Wanneer ze vanuit een intrinsieke motivatie aan hun ontwikkeling werken,
        zal dit beter werken dan wanneer ze door extrinsieke prikkels worden gemotiveerd.”</p3>

</div>




<div class="box2">

    <p5><b>Marieke van Oosterhout (Presentatiemotivatie ontwikkelen, z.d.):</b></p5>

</div>

<div class="container11">

    <p3>Iemand met een hoge prestatiemotivatie:<br>
        <dd>• is vaak intrinsiek gemotiveerd. Dat wil zeggen dat iemand van binnenuit geprikkeld wordt om een bepaalde activiteit te ondernemen vanwege het plezier dat hij hieraan beleeft.</dd>
        <dd>• heeft een voorkeur voor werkzaamheden die een beroep doen op zijn competenties.</dd>
        <dd>• kiest graag taken die net boven zijn kunnen liggen.</dd>
        <dd>• ervaart weinig faalangst.</dd><br>
        In allebei deze artikelen komen ze op de conclusie dat intrinsieke motivatie betere resultaten geeft. </p3>

</div>



