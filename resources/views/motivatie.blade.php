<div class="box2">

    <p5><b>Je uitslag</b></p5>

</div>

<div class="container11">

    <p3>Je bent in sterke mate extrinsiek gemotiveerd en redelijk intrinsiek gemotiveerd. <u>Dat je in sterke mate extrinsiek gemotiveerd bent betekent dat je het erg belangrijk vindt wat je met je studie kunt bereiken.</u><br>
        Je doet je best op school/in je studie omdat je later veel geld wilt verdienen, maatschappelijke status wilt hebben en/of erkenning van mensen wilt krijgen.<br>
        Op zich is het heel goed om bij dergelijke dingen stil te staan.<br>
        Je leeft immers niet in een sociaal isolement en moet straks wel je eigen boterham zien te verdienen.<br>
        Dat je redelijk intrinsiek gemotiveerd bent betekent dat je het ook best leuk vindt om te studeren. <u>Maar héél leuk vind je het niet.</u><br>
        <u>Voor jou is het resultaat van je studie belangrijker dan je studie zelf.</u><br>
        <br>
        Tip: Zolang je je studie leuk vindt is er niks aan de hand.
        <u>Kijk wel uit dat je een studie of beroep niet alléén maar kiest om anderen te plezieren of omwille van het geld.</u>
        Dan bestaat de kans dat je ongelukkig wordt.
        Je uitslag is tot stand gekomen na vergelijking met 2782 jongeren van 11 tot 26 jaar.
        Hierbij is gesteld dat van elke vijf jongeren er één hoog, één laag en drie redelijk gemotiveerd scoorden.</p3>

</div>




